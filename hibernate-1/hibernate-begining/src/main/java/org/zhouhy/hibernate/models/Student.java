package org.zhouhy.hibernate.models;

import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import java.sql.Timestamp;
import java.util.Objects;

@Entity
public class Student {
    private int id;
    private String name;
    private String sex;
    private Timestamp regdate;

    public Student(String name, String sex, Timestamp regDate) {
        super();
        this.name = name;
        this.sex = sex;
        this.regdate = regDate;
    }

    public Student() {
        
    }

    @Id
    @Column(name = "ID")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "NAME")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Basic
    @Column(name = "SEX")
    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    @Basic
    @Column(name = "REGDATE")
    public Timestamp getRegdate() {
        return regdate;
    }

    public void setRegdate(Timestamp regdate) {
        this.regdate = regdate;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Student student = (Student) o;
        return id == student.id && Objects.equals(name, student.name) && Objects.equals(sex, student.sex) && Objects.equals(regdate, student.regdate);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name, sex, regdate);
    }
}
